
package collectionsexample;


public class Student {
    
    private String firstName;
    private String lastName;

    public String getFirstName() {   ///sole purpose of this method is to get firstName from the class Student
        return firstName;
    }

    public void setFirstName(String firstName) {   ///sole purpose of this method is to set firstName in the class Student
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
    
    
}
